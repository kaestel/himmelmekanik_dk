				Intet menneske kan lide at blive forstyrret. Så simpelt er det vel. Og alligevel er vi ved helt 
				at glemme det uforstyrrede øjeblik. Vi lever i det evige nu, hvor alt sker hele tiden og konstant 
				og vi kigger på hinanden fra begge sider af skærmens lys. Jeg gør det selv. Jeg gør det mindre og 
				mindre. Jeg vil gerne gøre det mere mindre.
			</p>
			<p>
				Og derfor er jeg her. Sammen med dig og min nye plade, ‘Himmelmekanik'. Frugten af flere års tid, 
				kræfter, penge, blod, sved, spyt, snot og tårer. Alt sammen for at skabe et pusterum, en lytteoplevelse, 
				muligheden for refleksion og fordybelse.
			</p>
			<p>
				Lad os derfor sammen, nu, skabe et uforstyrret øjeblik, som ingen for altid vil kunne tage fra os.
			</p>
